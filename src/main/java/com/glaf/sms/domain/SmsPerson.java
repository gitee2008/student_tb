package com.glaf.sms.domain;

import java.io.Serializable;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

import com.alibaba.fastjson.JSONObject;
import com.fasterxml.jackson.databind.node.ObjectNode;
import com.glaf.core.base.JSONable;
import com.glaf.core.util.DateUtils;
import com.glaf.sms.util.SmsPersonJsonFactory;

/**
 * 
 * 待发短信的人员信息
 *
 */

@Entity
@Table(name = "SMS_PERSON")
public class SmsPerson implements Serializable, JSONable {
	private static final long serialVersionUID = 1L;

	@Id
	@Column(name = "ID_", length = 50, nullable = false)
	protected String id;

	/**
	 * 系统编号
	 */
	@Column(name = "CLIENTID_", length = 50)
	protected String clientId;

	/**
	 * 姓名
	 */
	@Column(name = "NAME_", length = 200)
	protected String name;

	/**
	 * 手机号码
	 */
	@Column(name = "MOBILE_", length = 20)
	protected String mobile;

	/**
	 * 限制条数
	 */
	@Column(name = "LIMIT_")
	protected int limit;

	/**
	 * 是否锁定
	 */
	@Column(name = "LOCKED_")
	protected int locked;

	/**
	 * 创建人
	 */
	@Column(name = "CREATEBY_", length = 50)
	protected String createBy;

	/**
	 * 创建日期
	 */
	@Temporal(TemporalType.TIMESTAMP)
	@Column(name = "CREATETIME_")
	protected Date createTime;

	public SmsPerson() {

	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		SmsPerson other = (SmsPerson) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	public String getClientId() {
		return this.clientId;
	}

	public String getCreateBy() {
		return this.createBy;
	}

	public Date getCreateTime() {
		return this.createTime;
	}

	public String getCreateTimeString() {
		if (this.createTime != null) {
			return DateUtils.getDateTime(this.createTime);
		}
		return "";
	}

	public String getId() {
		return this.id;
	}

	public int getLimit() {
		return this.limit;
	}

	public int getLocked() {
		return this.locked;
	}

	public String getMobile() {
		return this.mobile;
	}

	public String getName() {
		return this.name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	public SmsPerson jsonToObject(JSONObject jsonObject) {
		return SmsPersonJsonFactory.jsonToObject(jsonObject);
	}

	public void setClientId(String clientId) {
		this.clientId = clientId;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public void setId(String id) {
		this.id = id;
	}

	public void setLimit(int limit) {
		this.limit = limit;
	}

	public void setLocked(int locked) {
		this.locked = locked;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public void setName(String name) {
		this.name = name;
	}

	public JSONObject toJsonObject() {
		return SmsPersonJsonFactory.toJsonObject(this);
	}

	public ObjectNode toObjectNode() {
		return SmsPersonJsonFactory.toObjectNode(this);
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
	}

}
